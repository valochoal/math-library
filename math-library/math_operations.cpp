#include "math_operations.h"

// Implementación de la librería de operaciones matemáticas básicas

namespace MathLib {
    
    // Implementación de suma para números decimales
    double suma(double a, double b) {
        return a + b;
    }
    
    // Implementación de resta para números decimales
    double resta(double a, double b) {
        return a - b;
    }
    
    // Implementación de multiplicación para números decimales
    double multiplicacion(double a, double b) {
        return a * b;
    }
    
    // Implementación de suma para números enteros
    int suma(int a, int b) {
        return a + b;
    }
    
    // Implementación de resta para números enteros
    int resta(int a, int b) {
        return a - b;
    }
    
    // Implementación de multiplicación para números enteros
    int multiplicacion(int a, int b) {
        return a * b;
    }
}